function openNav() {
    document.getElementById("header__nav").style.width = "250px";
    document.getElementById("main").style.marginLeft = "250px";
    document.getElementById("title").style.marginLeft = "150px";
}
  
  /* Set the width of the side navigation to 0 */
  function closeNav() {
    document.getElementById("header__nav").style.width = "0";
    document.getElementById("main").style.marginLeft = "0";
    document.getElementById("title").style.marginLeft = "0";
}



